let fruits = ['blueberry', 'mango']

// push : add item at last
fruits.push('pineapple')
console.log(fruits)

// pop : remove last item
fruits.pop()
console.log(fruits)

// unshift : add item at starting
fruits.unshift('banana')
console.log(fruits)

// shift : remove 1st element
fruits.shift()
console.log(fruits)


// push, pop fast than shift, unshift

